package Algorithm;

public class RightTriangle {
    public static void main(String[] args) {
        for (int i = 0; i <5; i++ ) {
            for (int j = 1; j <=i ; j++) {
                System.out.printf("*");
            }
            System.out.println();
        }
    }
}
